package com.railways.users;

public class RailwayPassenger 
{
		private Integer passengerId;
		private String  passengerName;
		private String 	passengerMailId;
		private String 	passengerPassword;
		private String  passengerGender;
		private Byte 	passengerAge;
		private Long 	passengerMobileNumber;
		
		public Integer getPassengerId() {
			return passengerId;
		}
		
		public void setPassengerId(Integer passengerId) {
			this.passengerId = passengerId;
		}
		
		public String getPassengerName() {
			return passengerName;
		}
		
		public void setPassengerName(String passengerName) {
			this.passengerName = passengerName;
		}
		
		public String getPassengerMailId() {
			return passengerMailId;
		}
		
		public void setPassengerMailId(String passengerMailId) {
			this.passengerMailId = passengerMailId;
		}
		
		public String getPassengerPassword() {
			return passengerPassword;
		}
		
		public void setPassengerPassword(String passengerPassword) {
			this.passengerPassword = passengerPassword;
		}
		
		public String getPassengerGender() {
			return passengerGender;
		}
		
		public void setPassengerGender(String passengerGender) {
			this.passengerGender = passengerGender;
		}
		
		public Byte getPassengerAge() {
			return passengerAge;
		}
		
		public void setPassengerAge(Byte passengerAge) {
			this.passengerAge = passengerAge;
		}
		
		public Long getPassengerMobileNumber() {
			return passengerMobileNumber;
		}
		
		public void setPassengerMobileNumber(Long passengerMobileNumber) {
			this.passengerMobileNumber = passengerMobileNumber;
		}

}
